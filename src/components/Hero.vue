<script>
import Modal from './Modal.vue';
import Table from './Table.vue'

export default {
  components: {
    Modal,
    Table
  },

  data() {
    return {
      showModal: false,
      company: [],
      params : [],
      status : [],
      isOpen: false
    }
  },

  methods: {
    toggleModal() {
      this.showModal = !this.showModal
    },

    async loadCompany() {
      const req = await fetch('http://localhost:82/api/empresa.php?emp=1')
      const data = await req.json()
      console.log(data)
      this.company = data
    },

    async loadParams() {
      const req = await fetch('http://localhost:82/api/parametros.php?emp=1')
      const data = await req.json()
      console.log(data)
      this.params = data
      this.todayStatus(data)
    },

    async todayStatus(data) {
      let today = new Date()
      let day = today.getDay()
      let status = []
      const daysOfWeek = []

      for (let d in data) {
        daysOfWeek.push(data[d].dia)
      }

      status = data.filter(
        (x) => (x.dia === daysOfWeek[day])
      )

      this.status = status
     
      this.hourStatus(status)

      console.log('status', status)
      console.log('today', this.today)
    },

    async hourStatus(status) {
      let today = new Date()
      let timeNow = today.toLocaleTimeString()
      let open = status[0].abertura
      let closed = status[0].fechamento

      if (parseInt(timeNow) > parseInt(open) && 
          parseInt(timeNow) < parseInt(closed)
      ) {
        this.isOpen = true
      } else {
        this.isOpen = false
      }
    }
  }, 

  mounted() {
    this.loadCompany()
    this.loadParams()
  }
}
